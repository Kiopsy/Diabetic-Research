    //note: times and insulin MUST be parallel arrays
    public Injections(String[] times, double[] dosage){
        Position p = new Position(0,0);
        int columnPos = 0;
        int rowPos = 0;
        for(int i = 0; i<dosage.length; i++){
            p = Time.positionAt(times[i]);
            columnPos = p.getColumnPosition();
            rowPos = p.getRowPosition();
            //accounts for multiple inputs for one 5 minute slot
            if(injectionsArr[columnPos][rowPos] != 0.0){
                injectionsArr[columnPos][rowPos] = injectionsArr[columnPos][rowPos] + dosage[i];
            }
            else{
                injectionsArr[columnPos][rowPos] = dosage[i];
            }
        }
    }

    public void updateInjections(Position pos, double level)
    {
        injectionsArr[pos.getColumnPosition()][pos.getRowPosition()] = level;
    }

    public double[][] getInjectionsArr() {
        return injectionsArr;
    }
