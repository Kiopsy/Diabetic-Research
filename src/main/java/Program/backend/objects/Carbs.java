    //note: times and carbs MUST be parallel arrays
    public Carbs(String[] times, int[] carbs){
        Position p = new Position(0,0);
        int columnPos = 0;
        int rowPos = 0;
        for(int i = 0; i<carbs.length; i++){
            p = Time.positionAt(times[i]);
            columnPos = p.getColumnPosition();
            rowPos = p.getRowPosition();
            //accounts for multiple inputs for one 5 minute slot
            if(carbsArr[columnPos][rowPos] != 0){
                carbsArr[columnPos][rowPos] = carbsArr[columnPos][rowPos] + carbs[i];
            }
            else{
                carbsArr[columnPos][rowPos] = carbs[i];
            }
        }
    }

    public void setCarbs(String time, int carb)
    {
        Position p;
        p = Time.positionAt(time);
        carbsArr[p.getColumnPosition()][p.getRowPosition()] = carb;
    }

